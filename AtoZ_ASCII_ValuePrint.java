
public class AtoZ_ASCII_ValuePrint {

	public static void main(String[] args) {
		
		for(char i='A'; i<='Z'; i++) {
			System.out.print(i+"  ");
		}
		System.out.println();
		for(int i='A'; i<='Z'; i++) {
			System.out.print(i+" ");
		}
		System.out.println();
		System.out.println("===============================================================================");
		for(char i='a'; i<='z'; i++) {
			System.out.print(i+"  ");
		}
		System.out.println();
		for(int i='a';i<='z'; i++) {
			System.out.print(i+" ");
		}
		
	}

}
